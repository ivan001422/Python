# -*- coding: utf-8 -*-
"""
Класс Point — точка на плоскости.
ООП-концепции: класс, объект, атрибуты, методы, инкапсуляция, docstring.
"""

class Point:
    """Точка в двумерном пространстве."""

    def __init__(self, x: float = 0, y: float = 0) -> None:
        """
        Конструктор.
        Концепции: __init__, self, параметры по умолчанию, аннотации типов,
        вызов другого метода (переиспользование кода).
        """
        self.move(x, y)

    def move(self, x: float, y: float) -> None:
        """
        Перемещение в новые координаты.
        Концепции: метод, self, изменение состояния, точечная нотация,
        возврат None (ничего не возвращаем).
        """
        self.x = x
        self.y = y

    def reset(self) -> None:
        """
        Сброс в (0, 0).
        Концепции: переиспользование методов (DRY), вызов move(0, 0).
        """
        self.move(0, 0)

    def dis(self, other: 'Point') -> float:
        """
        Евклидово расстояние до другой точки.
        Концепции: передача объекта-аргумента, forward reference ('Point'),
        доступ к атрибутам другого объекта, вычисление и возврат значения.
        """
        return ((self.x - other.x) ** 2 + (self.y - other.y) ** 2) ** 0.5


# Тестирование
point1 = Point()              # (0, 0)
point2 = Point(1, 1)          # (1, 1)
point2.move(3, 4)             # переместили в (3, 4)
print(point1.dis(point2))     # 5.0
